CREATE TABLE IF NOT EXISTS `disc` (
  `id` int(11) unsigned NOT NULL AUTO_INCREMENT,
  `artist_id` int(11) unsigned NOT NULL,
  `title` varchar(255) NOT NULL,
  `description` text NOT NULL,
  `release_time` datetime NOT NULL,
  `force_price` smallint(4) NOT NULL DEFAULT '0' COMMENT 'iNDIEVOX 代管廠牌可強制設定專輯總價',
  `disc_type_id` tinyint(2) unsigned NOT NULL DEFAULT '0',
  `genre` smallint(3) unsigned NOT NULL DEFAULT '7',
  `producer` varchar(100) NOT NULL,
  `label` varchar(100) NOT NULL,
  `sub_label_id` int(11) NOT NULL,
  `upc_code` varchar(12) CHARACTER SET utf8mb4 NOT NULL,
  `is_released` tinyint(1) unsigned NOT NULL DEFAULT '0',
  `is_deleted` tinyint(1) unsigned NOT NULL DEFAULT '0',
  `create_time` datetime NOT NULL,
  `modify_time` datetime NOT NULL,
  `delete_time` datetime NOT NULL DEFAULT '0000-00-00 00:00:00',
  PRIMARY KEY (`id`),
  KEY `artist_id` (`artist_id`),
  KEY `release_time` (`release_time`),
  KEY `disc_type_id` (`disc_type_id`),
  KEY `genre` (`genre`),
  KEY `sub_label_id` (`sub_label_id`),
  KEY `is_released` (`is_released`),
  KEY `is_deleted` (`is_deleted`)
) ENGINE=InnoDB  DEFAULT CHARSET=utf8 AUTO_INCREMENT=8444 ;