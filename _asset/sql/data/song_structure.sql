CREATE TABLE IF NOT EXISTS `song` (
  `id` int(11) unsigned NOT NULL AUTO_INCREMENT,
  `artist_id` int(11) unsigned NOT NULL,
  `disc_id` int(11) unsigned NOT NULL,
  `disc_order` smallint(3) unsigned NOT NULL,
  `title` varchar(255) NOT NULL,
  `description` text NOT NULL,
  `release_time` datetime NOT NULL,
  `year` year(4) NOT NULL,
  `price` smallint(3) unsigned NOT NULL DEFAULT '19',
  `price_type` tinyint(1) unsigned NOT NULL DEFAULT '0' COMMENT '0-price 訂價, 1-noprice 隨意付, 2-demoonly 僅試聽',
  `genre` smallint(3) unsigned NOT NULL DEFAULT '7',
  `lyric` text NOT NULL,
  `distribution_lyric` text NOT NULL,
  `total_sec` smallint(5) NOT NULL,
  `artist_name` varchar(100) NOT NULL,
  `song_composer` varchar(100) NOT NULL,
  `lyricist` varchar(100) NOT NULL,
  `producer` varchar(100) NOT NULL,
  `label` varchar(100) NOT NULL,
  `isrc_code` varchar(20) NOT NULL,
  `radio_playable` tinyint(1) unsigned NOT NULL DEFAULT '1',
  `is_released` tinyint(1) unsigned NOT NULL DEFAULT '0',
  `is_deleted` tinyint(1) unsigned NOT NULL DEFAULT '0',
  `create_time` datetime NOT NULL,
  `modify_time` datetime NOT NULL,
  `delete_time` datetime NOT NULL DEFAULT '0000-00-00 00:00:00',
  PRIMARY KEY (`id`),
  KEY `artist_id` (`artist_id`),
  KEY `disc_id` (`disc_id`),
  KEY `disc_order` (`disc_order`),
  KEY `release_time` (`release_time`),
  KEY `price_type` (`price_type`),
  KEY `genre` (`genre`),
  KEY `radio_playable` (`radio_playable`),
  KEY `is_released` (`is_released`),
  KEY `is_deleted` (`is_deleted`),
  KEY `is_released_is_deleted` (`is_released`,`is_deleted`),
  KEY `title` (`title`)
) ENGINE=InnoDB  DEFAULT CHARSET=utf8 AUTO_INCREMENT=95601 ;